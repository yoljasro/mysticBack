async function updateTarot() {
    try {
        await mongoose.connect(process.env.MONGO_URI);
        console.log('Connected to MongoDB for update...');

        for (const cardData of enrichedData) {
            await TarotCard.findOneAndUpdate(
                { name: cardData.name },
                { $set: cardData },
                { new: true }
            );
            console.log(`Updated card: ${cardData.name}`);
        }

        console.log('Update complete!');
        process.exit(0);
    } catch (err) {
        console.error('Update error:', err);
        process.exit(1);
    }
}

updateTarot();
