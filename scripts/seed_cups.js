async function seedCups() {
    try {
        await mongoose.connect(process.env.MONGO_URI);
        console.log('Connected to MongoDB...');

        for (const card of cupsData) {
            const result = await TarotCard.findOneAndUpdate(
                { name: card.name },
                { $set: card },
                { new: true, upsert: true }
            );
            console.log(`Seeded/Updated: ${result.name}`);
        }

        console.log('Cups Seeding Complete!');
        process.exit(0);
    } catch (err) {
        console.error('Seeding error:', err);
        process.exit(1);
    }
}

